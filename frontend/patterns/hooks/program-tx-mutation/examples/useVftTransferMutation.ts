import type { HexString } from '@gear-js/api';
import { useProgram } from '@gear-js/react-hooks';

import { Program } from '@/lib/extended-vft';
import { GAS_LIMIT } from '@/frontend/shared/constants/gas';

import { usePrepareProgramTx } from '../../prepare-program-tx/usePrepareProgramTx';
import { useProgramTxMutation } from '../useProgramTxMutation';

/**
 * Domain-specific parameters required for a VFT transfer.
 *
 * These are the values coming from the UI layer and mapped
 * to the arguments expected by the program.
 */
type Params = {
  from: HexString;
  to: HexString;
  value: string;
};

/**
 * Example hook demonstrating how to compose two patterns:
 *
 * - usePrepareProgramTx: prepares a transaction
 * - useProgramTxMutation: executes the prepared transaction
 *
 * This hook represents a complete “prepare → execute” flow
 * for a VFT token transfer.
 */
export function useVftTransfer(programId?: HexString) {
  /**
   * Resolve the on-chain program instance using Gear hooks.
   *
   * The `Program` class is typically generated by Sails
   * and contains type-safe bindings to the smart contract.
   */
  const { data: program } = useProgram({
    library: Program,
    id: programId,
  });

  /**
   * Phase 1: Transaction preparation
   *
   * This step only builds the transaction object but does not execute it.
   * It validates prerequisites (program + account) and maps UI params
   * into program arguments.
   */
  const { prepare, canPrepare } = usePrepareProgramTx<Params>({
    program,
    serviceName: 'vft',
    functionName: 'transfer',
    mapArgs: ({ from, to, value }) => [from, to, value],
    gasLimit: GAS_LIMIT,
  });

  /**
   * Phase 2: Transaction execution
   *
   * This mutation is responsible only for executing an already
   * prepared transaction and exposing React Query state.
   */
  const mutation = useProgramTxMutation();

  /**
   * High-level domain action that composes both phases:
   *
   * 1. Prepare the transaction using domain parameters
   * 2. Execute it via React Query mutation
   */
  const transfer = async (params: Params) => {
    const tx = await prepare(params);
    return mutation.mutateAsync(tx);
  };

  /**
   * Expose a friendly API for UI components:
   *
   * - `canTransfer`: indicates if the action can be triggered
   * - `transfer`: domain-specific action
   * - React Query mutation state (isPending, error, etc.)
   */
  return {
    canTransfer: canPrepare && !mutation.isPending,
    transfer,
    ...mutation,
  };
}
